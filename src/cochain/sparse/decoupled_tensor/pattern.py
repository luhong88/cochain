@dataclass(frozen=True)
class BlockDiagConfig:
    """
    Block-diagonal sparse tensor pattern.

    This immutable dataclass encodes information on the structure of a block-diagonal
    `SparseDecoupledTensor`. It is generated by `pack_block_diag()` during block
    diagonal construction and consumed by `unpack_block_diag()` to split a
    block-diagonal tensor into its constituent tensors.

    Parameters
    ----------
    batch_perm : [nz,]
        An int64 tensor that represents the permutation required to sort the
        row-major concatenated COO index tensor to a batch-major tensor. If the
        tensor has no batch dimension, this tensor contains the identity permutation.
    nnzs : [b,]
        The number of nonzero elements per block.
    pattern_shapes : [b,]
        The shape of block.

    Attributes
    ----------
    batch_perm_inv : [nz,]
        The inverse of the batch_perm permutation.

    Notes
    -----
    Block-diagonal `SparseDecoupledTensor`s derived from block-diagonal mesh
    batching do not have `BlockDiagConfig`s. To unpack such tensors, see the
    `unpack_by_ptrs()` method of `SparseDecoupledTensor`.
    """

    batch_perm: Int64[Tensor, " nz"]
    nnzs: list[int]
    pattern_shapes: list[torch.Size]

    def to(self, *args, **kwargs) -> BlockDiagConfig:
        """
        Call to() on attached tensor batch_perm.

        Note that dtype conversions are ignored.
        """
        device, dtype, copy_flag, non_blocking, memory_format = parse_to(
            *args, **kwargs
        )
        new_batch_perm = self.batch_perm.to(
            device=device,
            copy=copy_flag,
            non_blocking=non_blocking,
            memory_format=memory_format,
        )
        return BlockDiagConfig(new_batch_perm, self.nnzs, self.pattern_shapes)

    @property
    def batch_perm_inv(self) -> Int64[Tensor, " nz"]:
        """
        The inverse of the batch_perm permutation.

        Note that this scattering approach is strictly better than
        `torch.argsort(batch_perm)` for a permutation.
        """
        nnz = self.batch_perm.size(0)

        inv = torch.empty_like(self.batch_perm)
        inv[self.batch_perm] = torch.arange(
            nnz, dtype=self.batch_perm.dtype, device=self.batch_perm.device
        )

        return inv


